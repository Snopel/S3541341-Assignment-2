package methods;

public class Calculate {
	
	public double add(double a, double b) {
		double ansAdd = a+b;
		return ansAdd;
	}
	
	public double subtract(double a, double b) {
		double ansSub = a-b;
		return ansSub;
	}
	
	public double multi(double a, double b) {
		double ansMulti = a*b;
		return ansMulti;
	}
	
	public double divis(double a, double b) {
		double ansDivi = a/b;
		return ansDivi;
	}

}
